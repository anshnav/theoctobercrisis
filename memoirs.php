<div class="f2f-section-1" id="f2f-section-1">

    <div class="author-1-wrapper fadein">

        <img src="images/quote-icon.svg" alt="">
        <h2>Dave Meredith
        </h2>

    </div>


    <div class="author-1-text">
                <p class="fadein"><span class="drop-cap">I</span> was a young trooper with the 8th Canadian Hussars (Princess Louise’s) at CFB Petawawa when the October Crisis of 1970 took place. I was at home with my new bride, who knew nothing of military life, when the phone rang and everything turned upside down.
                </p><br><br>

                <p class="fadein">“Report in now” came the order.
                </p><br><br>
                
                <p class="fadein">Grabbing my already packed kit, I kissed my wife goodbye not knowing where I was heading or when I would be back. Arriving at the hanger, we were formed up and a head count was done. “Mount up, we are heading to Ottawa.”
                </p><br><br>
                
                <p class="fadein">The two-hour drive down Highway 17 in tracked vehicles took us through Pembroke, Renfrew and Arnprior to Connaught Ranges.
                </p><br><br>

                <p class="fadein">On arrival, we mounted the .50 calibre machine guns and loaded live ammunition. Whatever was about to happen just became real! Then we grabbed our kit and were trucked to our new home at the Canadian Forces Reserve Barrack Dow’s Lake armoury.
                </p><br><br>

                <p class="fadein">One night my troop was ordered to go to the Kanata Hill on Highway 417 to stop all traffic from leaving Ottawa as a diplomat had just been kidnapped. Traffic was lined up as far as you could see as we stood with guns
                </p><br><br>

                <p class="fadein">This is one memory of the October Crisis I’ll never forget.
                </p><br><br>
                
                
    </div>

</div>  


