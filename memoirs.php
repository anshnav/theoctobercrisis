 <div class="f2f-section-1" id="f2f-section-1">

    <div class="author-1-wrapper fadein">

            <img src="images/quote-icon.svg" alt="">
            <h2>Nathan Cooper
            </h2>

    </div>


    <div class="author-1-text">
        <p class="fadein"><span class="drop-cap">I</span>t seems a little-known fact that the communications facility at National Defence Headquarters on Colonel By Drive in Ottawa was the target of an FLQ bombing. </p><br><br>

                <p class="fadein">The bomb was placed in a window well on the ground floor of B Building where the Tape Relay Centre (TRC) was located. That area was literally blown apart and a DND civilian employee—a French Canadian—was killed. </p><br><br>
                
                <p class="fadein">I was living in Beaver Barracks on Metcalfe Street at the time and remember vividly being “recruited” while in the washroom facilities early one morning by the military police to help out at the scene. No details were provided other than some “incident” had taken place at Cartier Square. </p><br><br>
                
                <p class="fadein">What the military police failed to realize was that I and another “recruit” actually worked there. As we arrived at the scene there was total confusion, with vehicles parked every which way and people wandering around in a confused state. We were appointed to crowd control.</p><br><br>
                
                <p class="fadein">Still having no idea of what had happened, I requested a weapon. I didn’t get one and later learned that the corner of the TRC room where I would have been later that morning had been blown up.</p><br><br>
                
                <p class="fadein">I was eventually released from crowd control duties and allowed to enter my workplace. The TRC as we knew it no longer existed. This meant that a large portion of the DND communications network was down, so our main priority was to get it restored ASAP.
                </p><br><br>

                <p class="fadein">That was the least-onerous task in trying to get everything back up and running. Knowing we had been attacked in our own country—and in downtown Ottawa—did not help. However, if I recall correctly, the TRC was back on-line before midnight.
                </p> <br><br>
                
                
    </div>



