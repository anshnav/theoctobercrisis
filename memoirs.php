<div class="f2f-section-1" id="f2f-section-1">

    <div class="author-1-wrapper fadein">

        <img src="images/quote-icon.svg" alt="">
        <h2>Gregory A. Milne
        </h2>

    </div>


    <div class="author-1-text">
                <p class="fadein"><span class="drop-cap">I</span>n 1970, I was chaplain to 3rd Regiment, Royal Canadian Horse Artillery stationed in CFB Shilo. On the Sunday morning that Pierre Laporte’s body was discovered, the regiment, which was already on standby, was given 12 hours to be on the way to Montreal. I was on board a Yukon aircraft leaving CFB Rivers just before midnight that evening.

                </p><br><br>

                <p class="fadein">The unit was garrisoned at CFB Longue-Pointe on Hochelaga Ave. in Montreal, and within hours assignments for various guard duties were given to the troops. Many were guarding VIPs or electrical installations. These were mostly outside jobs, often in the cold and rain. The troops would work in pairs on four-hour shifts, with an eight-hour rest period in tent groups. The troops themselves were very imaginative in sorting out their various living conditions, including obtaining televisions, etc. Some even managed to talk local citizens into letting them live in their garages. 
                </p><br><br>
                
                <p class="fadein">As a chaplain, I knew my duty would be to visit the troops in the field to check on their morale and the conditions of their living situations. I could bring them magazines and books to read in their off time. I could carry a couple of packs of cigarettes in case anyone needed them. To do my duty, I was assigned a jeep and a driver. The problem, however, was that an order had come down from on high that no soldier was to leave the garrison unless they were armed, and could prove their armament to the military police guarding the entry and exit to the compound.
                </p><br><br>
                
                <p class="fadein">Problem indeed. Everyone knows chaplains do not bear arms, and indeed, being unarmed is their greatest strength. There were several regiments garrisoned around Montreal, and all of the chaplains were stuck inside their garrisons due to the armament order. I made the decision that this would not stop me. I went to stores and signed out a Browning pistol and the standard 10 rounds. I told my driver that if he told anyone, I would give him a pass to hell. He was armed with a Sterling submachine gun, which was held in a bracket ahead of his seat. I gave him the pistol to carry on his web belt. When we got to the gate, he produced the submachine gun on his side and I produced the pistol and the loaded clip on mine.
                </p><br><br>

                <p class="fadein">Once outside the garrison, the pistol went back into the holster on the driver’s belt. No one was going to question me after that as to whether or not I was armed.
                </p><br><br>

                <p class="fadein">At breakfast on the third day, the commanding officer asked me how I managed to get out the gate, as it had been reported to him that the troops appreciated the fact that they had been visited by the padre. At first, I told him, “You don’t really want to know sir.”
                </p><br><br>

                <p class="fadein">He laughed, and said, “Fess up, Padre!” 
                </p><br><br>

                <p class="fadein">So I told him, and then he went to work to make sure that all the padres could get out to do their job. Within a few days, an order came down that if there were three people in a jeep, only two of them needed to be armed. I returned the pistol to stores and asked for volunteers to “ride shotgun” with the padre. There was never a shortage of volunteers. 
                </p><br><br>

                <p class="fadein">I was always accompanied by an assistant who would do his best to add to the humour and comradeship of the unit. The morale in 3RCHA remained very good throughout that operation. We returned to CFB Shilo after a five-week stint, being replaced by 2nd Battalion, Princess Patricia's Canadian Light Infantry, which was then stationed in Winnipeg. 
                </p><br><br>
                
                
    </div>

</div>  


