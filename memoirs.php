    <div class="author-1-wrapper fadein">

        <img src="images/quote-icon.svg" alt="">
        <h2>PAT GAGNE
        </h2>

    </div>


    <div class="author-1-text">
                <p class="fadein"><span class="drop-cap">A</span> few of us transferred from Toronto to Montreal to work at the CPR Angus shop. We lived in the Sherbrooke and Pie IX area, a hot spot for communiqués from the FLQ. Then we moved to Guy and de Maisonneuve, near Police Station 10, another hot spot.
                </p><br><br>

                <p class="fadein">Soldiers and police were everywhere. Their comment to us was “Behave.” We did.
                </p><br><br>
                
                <p class="fadein">It was an uneasy time. Bars were full and people just taking things one day at a time.
                </p><br><br>
                
                <p class="fadein">Montreal is exciting at the best of time, but this was surreal. I’ll never forget my time in Montreal. It’s a great city.</p><br><br>
                
                
    </div>

