    <div class="f2f-section-3" id="f2f-section-1">

            <div class="author-1-wrapper fadein">
        
                <img src="images/quote-icon.svg" alt="">
                <h2>Greg Sheen
                </h2>
        
            </div>
        
        
            <div class="author-1-text">
                        <p class="fadein"><span class="drop-cap">I</span> was a driver in the Transport Platoon, 5 Service Battalion at CFB Valcartier in October 1970.
                        </p><br><br>
        
                        <p class="fadein">British diplomat James Cross had been kidnapped and we were advised to get our personal gear ready for deployment, if required. Then Quebec Deputy Premier Pierre Laporte was kidnapped.
                        </p><br><br>
    
                        <p class="fadein">Later, at home, my wife answered a phone call in the middle of the night. She advised me, “the caller said ‘bluebird.’” That was the ‘fan out’ signal to pack up and head off to the base.
                        </p><br><br>
    
                        <p class="fadein">For the next two to three days, we remained on base in Valcartier, preparing our vehicles and equipment. We were also confined to the base, sleeping on the floor of the transport garage. We were not even allowed to contact family, to let them know where we were or what was going on.
                        </p><br><br>

                        <p class="fadein">Eventually we formed up in military vehicle convoys and headed to Montreal. Ironically, our route into Montreal took us right past the St. Hubert airport, where the vehicle containing Laporte’s body was later discovered.
                            </p><br><br>

                        <p class="fadein">We were housed at CFB Longue-Pointe in Montreal. Most of the days were typical military routine, but there were regular off-base duties to deliver supplies to various military posts in and around Montreal.
                        </p><br><br>

                        <p class="fadein">One assignment I had was the delivery of a load of “concertina wire” to Sorel, Que., east of Montreal, very late one night. It was all very secretive, and I was only told to go to the Sorel police office and wait to be contacted there. Eventually an individual showed up and escorted my vehicle to an area somewhere outside of Sorel, where, after passing though several checkpoints in the darkness and accompanied by guards riding the running board of the truck, we reached the destination.
                            </p><br><br>

                        <p class="fadein">The concertina wire was off-loaded and I then learned that I was on the grounds of the Simard family home, owned by the in-laws of Quebec Premier Robert Bourassa. He was staying there. I was advised that the wire was for the shoreline of the Simard property, which was along the river. The family were the the owners of the shipbuilding company Marine Industries Limited.
                                </p><br><br>

                         <p class="fadein">After several weeks in Montreal, some of us returned to our base in Valcartier. I was assigned to assist the military police. Not quite as interesting as it might sound, as the rest of the crisis was spent guarding and patrolling—on foot—Valcartier’s isolated ammunition depot.
                        </p><br><br>
                        
             
            </div>
        
