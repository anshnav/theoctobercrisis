            <div class="author-1-wrapper fadein">
        
                <img src="images/quote-icon.svg" alt="">
                <h2>Stephen Walsh
                </h2>
        
            </div>
        
        
            <div class="author-1-text">
                        <p class="fadein"><span class="drop-cap">I</span> was in Rosemere High School when the October crisis transpired. At the time, I found it quite worrisome and frightening. I remember it very well. 

                        </p><br><br>
        
                        <p class="fadein">The father of two of my friends was stationed at Camp-Bouchard, a military base in Blainville, Que., not far from our school. I remember vividly the helicopters going back and forth from the camp.
                        </p><br><br>
    
                        <p class="fadein">My friends would give us updates as to what was happening with the military in our area. For a teenager, this was an exciting but frightening time. Having martial law declared, to me, was a big worry. I was always wondering if it would escalate and that actual fighting between the FLQ (and it’s supporters) would culminate. Fortunately, this did not happen. But changes in Quebec did follow.
                        </p><br><br>
    
                        <p class="fadein">It has left an everlasting impression on me. Maybe because I was just 16 at the time.
                        </p><br><br>
                        
             
            </div>
        
