            <div class="author-1-wrapper fadein">
        
                <img src="images/quote-icon.svg" alt="">
                <h2>Rod Hodgson, Hudson, Que.
                </h2>
        
            </div>
        
        
            <div class="author-1-text">
                        <p class="fadein"><span class="drop-cap">I</span> was a freshman at Macdonald College of McGill University in Sainte-Anne-de-Bellevue, Que., in 1970. 

                        </p><br><br>
        
                        <p class="fadein">In late October or early November, while I was just about to hand in a chemistry report to my professor, a police officer walked into our chemistry lab carrying a shotgun and announced that the class must vacate the building immediately. I handed in my report and as I went out the other door of the lab I met another police officer walking in carrying a machine gun!
                        </p><br><br>
    
                        <p class="fadein">I left by the nearest exit and walked across campus and came upon most of the other students milling around and scores of police and a helicopter circling overhead.
                        </p><br><br>
    
                        <p class="fadein">One of the Rose brothers, Paul I believe, had reportedly been seen on campus. His mother was living in Sainte-Anne-de-Bellevue at the time. One of the buildings was searched but he was not located.
                        </p><br><br>
    
                        <p class="fadein">My father was also a police officer in Hudson, Que., and thus I knew the dangers and potential troubles. Luckily, it all worked out, but it sure hit home to say the least. 
                        </p><br><br>

                        <p class="fadein">I was very impressed with how Prime Minister Trudeau handled himself on the balcony of Montreal City Hall too. 
                            </p><br><br>
                        
             
            </div>
        
