<div class="f2f-section-2" id="f2f-section-1">

        <div class="author-1-wrapper fadein">
    
            <img src="images/quote-icon.svg" alt="">
            <h2>Elizabeth Smith, Tsawwassen, B.C.
            </h2>
    
        </div>
    
    
        <div class="author-1-text">
                    <p class="fadein"><span class="drop-cap">I</span> was in my final year in the science program at McGill University in 1970.
                    </p><br><br>
    
                    <p class="fadein">Our limnology (freshwater biology) course went on a field trip to Mont Saint-Hilaire east of Montreal.
                    </p><br><br>

                    <p class="fadein">The FLQ crisis in Montreal was taking a toll on everyone and someone on our bus had a portable radio. As we were returning home, the news broke that Pierre Laporte’s remains had been located in a car trunk and that he had been strangled. 
                    </p><br><br>

                    <p class="fadein">We were all in shock and many tears were shed on that ride home to McGill.
                    </p><br><br>

                    <p class="fadein">There was still no news about the whereabouts of James Cross, the British Trade Commissioner. The next day, Sunday, Oct. 18, Prime Minister Pierre Trudeau enacted the War Measures Act. Terrorism as we now know it had arrived in Canada.
                    </p><br><br>

                    <p class="fadein">Throughout the rest of the crisis, mailboxes were welded shut and there were armed personnel everywhere—even stationed at the entrance to the Roddick Gates at the entrance to McGill. Most of us in that class grew up pretty quickly. Remembrance of that time, at least for me, will never fade.
                    </p><br><br>
                    
         
        </div>
    
</div>  



