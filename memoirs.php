                </p><br><br>

                <p class="fadein">We boarded a plane in Toronto and flew to Montreal to change planes. In the process we had to walk through a corridor of army and police fully armed with machine guns trained on us. We were very nervous about this situation.
                </p><br><br>
                
                <p class="fadein">No one would tell us what was going on, just that we should walk past these military personal with guns.
                </p><br><br>
                
                <p class="fadein">I remember being very nervous. It was scary to be present when Pierre Laporte and James Cross were being kidnapped.
                </p><br><br>
     
                
