<div class="f2f-section-2" id="f2f-section-2">

    <div class="author-2-wrapper fadein">

            <img src="images/author-2.svg" alt="">
            <p>J.L. Granatstein says</p>
            <h2>NO</h2>

    </div>


    <div class="author-2-blockquote fadein">
        <h2>“Let us be clear: Ottawa and Quebec City blinked.”</h2>
    </div>

    <div class="author-2-text">
                <p class="fadein"><span class="drop-cap">I</span>n her memoir Beyond Reason, Margaret Trudeau wrote that her husband warned her in October 1970 that if she or baby Justin were to be kidnapped, there would be no negotiations to free her.
                </p><br><br>

                <p class="fadein">“You mean you would let them kill me rather than agree to terms?” she asked. 
                    “Yes, I would,” was Pierre Trudeau’s answer. In other words, Canada would never negotiate with terrorists.
                </p><br><br>
                
                <p class="fadein">But when the Front de libération du Québec seized James Cross, the British trade commissioner in Montreal, on Oct. 5 and followed this with the kidnapping of Quebec labour minister Pierre Laporte, Canada did negotiate with the terrorists and did permit the Cross kidnappers to have safe passage to Cuba.
                </p><br><br>
                
                <p class="fadein">Trudeau’s tough-sounding stance—“Just watch me”—and his imposition of the War Measures Act have blurred the public’s memory.</p><br><br>
                
                <p class="fadein">The FLQ cell that held Cross released a list of demands for his release: stop the police searches for them; free 23 FLQ “political” prisoners; broadcast their manifesto; give them $500,000 in gold bullion; and arrange sanctuary for the cell members in Algeria or Cuba. Accept the demands, the cell stated, or Cross would die. 
                </p><br><br>
                
                <p class="fadein">Because Cross was a diplomat, foreign minister Mitchell Sharp took responsibility for the immediate response and informed Parliament that he had consulted London and Quebec City and all had rejected the FLQ demands.
                </p><br><br>

                <p class="fadein">But on Oct. 8, Sharp began the process of negotiation by permitting the broadcast of the FLQ manifesto, a vicious diatribe that called Trudeau a “fairy” and Montreal mayor Jean Drapeau a “dog.” The prime minister apparently had not been consulted and was very angry. The FLQ cell, however, was delighted, clearly believing their demands were to be met.
                </p> <br><br>
                
                <p class="fadein">On Oct. 10, Quebec Justice Minister Jérôme Choquette went on television to announce there would be no negotiations. But he then offered a major concession: safe conduct to a foreign country. That night, Laporte was kidnapped.
                </p> <br><br>

                <p class="fadein">The next day, after consulting Trudeau, Bourassa again opened the door to negotiations and on Oct. 12, appointed a lawyer to talk terms with an FLQ lawyer. Soon, a group of 16 Quebec notables, believing that no price was too high to save Cross and Laporte, urged Ottawa and Quebec City to negotiate. With troops on the streets in Quebec and Ottawa, with the War Measures Act in effect on Oct. 16, and with Laporte found murdered in the trunk of a car, the rest was police work.
                </p> <br><br>

                <p class="fadein">The FLQ hideout was found. Cross was freed on Dec. 4. Ottawa had negotiated with Cuba to let the five kidnappers fly there in an air force aircraft, the last stage of the affair. The murderers of Laporte were caught on Dec. 28.Let us be clear: Ottawa and Quebec City blinked. While the Laporte murderers were jailed, the Cross kidnappers escaped to Cuba. The FLQ had achieved its aims in arousing separatist-leaning Québécois with its manifesto and by securing asylum. This was no victory for toughness, and Margaret Trudeau, we must assume, need not have feared for her life. 
                </p> <br><br>

