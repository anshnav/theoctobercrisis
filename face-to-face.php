    <div class="author-1-wrapper fadein">

            <img src="images/author-1.svg" alt="">
            <p>D’Arcy Jenish says</p>
            <h2>YES</h2>

    </div>


    <div class="author-1-blockquote fadein">
        <h2>“They opted for a sensible way to bring the kidnapping crisis to a peaceful conclusion.”</h2>
    </div>

    <div class="author-1-text">
        <p class="fadein"><span class="drop-cap">I</span>n the early morning hours of Dec. 3, 1970, dozens of police officers and heavily armed soldiers surrounded a modest dwelling in Montreal North where, for nearly two months, British diplomat James Cross had been held hostage by a gang of would-be revolutionaries who styled themselves the Liberation Cell of the Front de libération du Québec. </p><br><br>

                <p class="fadein">Their situation was hopeless, but the kidnappers remained defiant. They warned the authorities via a handwritten communiqué that if the police attempted to storm the hideout with guns or tear gas, Cross would be the first to die. And they were in possession of two sawed-off .30-calibre carbines, two handguns and eight pounds of dynamite.</p><br><br>
                
                <p class="fadein">Rather than risk Cross’s life, the authorities wisely agreed to grant the kidnappers safe passage to Cuba in exchange for releasing the diplomat. They opted for a sensible way to bring the kidnapping crisis to a peaceful conclusion and the passage of time proved the wisdom of their decision. </p><br><br>
                
                <p class="fadein">Exile in Cuba was a nightmare for the kidnappers. They were confined to hotels in Havana for months at a time. They suffered from excruciating boredom. Dissension and disagreements festered among them. Jacques Cossette-Trudel and his wife Louise Lanctôt renounced terrorism, which caused a rift between them and Louise’s brother Jacques, the leader of the gang.</p><br><br>
                
                <p class="fadein">By the fall of 1973, the exiles were all desperate to escape Cuba. In July 1974, the Cubans finally provided them with travel documents and airfare allowing them to fly to Prague and from there to Paris.</p><br><br>
                
                <p class="fadein">Life in the French capital proved dismal as well, at least for the Lanctôts and Cossette-Trudels, who were stuck in menial, low-end jobs and shabby, suburban apartments. Besides, they were desperately homesick. In December 1978, the Cossette-Trudels took their chances and returned. Lanctôt and family arrived in January 1979, Marc Carbonneau returned in May 1981 and Yves Langlois in June 1982.</p><br><br>

                <p class="fadein">All were arrested immediately on landing in Canada and charged with various offences related to the Cross kidnapping. They had their days in court and Carbonneau received the lightest sentence—20 months—and Lanctôt the stiffest—three years—because he was the leader of this criminal faction and was unrepentant.</p> <br><br>
                
                <p class="fadein">Among some Anglo Montrealers as well as the English-language media, there were murmurs of discontent over the perceived lenience of the courts, but the francophone press concluded that the cases had been handled properly.
                All in all, then, it is safe to say that the case of the Cross kidnappers was appropriately handled, first by Canadian authorities and later by the courts. The authorities liberated the diplomat without a bloodbath and the courts levied reasonable sentences which ensured that the kidnappers would all get a taste of life behind bars. Living as exiles was nowhere near as grim as time served in a federal penitentiary, but their lives in Cuba and Paris had been miserable and they returned to Canada older, chastened and a little wiser.
                </p><br><br>

