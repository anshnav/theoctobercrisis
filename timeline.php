          <h2 class="timeline__content-title"></h2>
          <p class="timeline__content-desc">Paul Rose, Jacques Rose, and Francis Simard, the remaining three members of the Chenier cell, were arrested. Along with Bernard Lortie, they were charged with kidnapping and murder. Paul Rose and Francis Simard later received life sentences for murder. Bernard Lortie was sentenced to 20 years for kidnapping. Jacques Rose was initially acquitted but was later convicted of being an accessory and sentenced to eight years in prison.
            </p>
