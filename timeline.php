                <p>One of the most tumultuous periods in Canadian history took place between 1963 and 1970 – a period that saw political kidnappings, violence and soldiers on the streets of Quebec and the nation’s capital.</p>

                <p>This timeline highlights the main events leading up to and at the height of the October Crisis, when the FLQ – a separatist group promoting an independent Quebec – kidnapped a cabinet minister and a British trade commissioner, prompting the federal government to enact the War Measures Act.</p>
