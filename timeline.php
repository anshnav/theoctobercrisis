        <h2 class="timeline__content-title"></h2>
        <p class="timeline__content-desc">Prime Minister Trudeau announced the proclamation of the War Measures Act. First adopted by the Canadian Parliament on 22 August 1914 at the onset of World War I, the legislation granted broad authority to the Canadian government to maintain security and order in times of war or civil unrest. Those considered “enemy aliens” were subject to the suspension of their civil rights and liberties. The War Measures Act was also invoked during World War II, resulting in numerous searches, arrests, and detention without the benefit of charge or trial. (The War Measures Act has since been replaced by the Emergencies Act which is more limited in scope.</p>
