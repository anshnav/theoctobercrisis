                  <div class="art-section-5-text-container">

                  <p class="fadein"><b>The FLQ</b> formed in February 1963, immediately taxing the police force’s sole bomb-disposal expert. In April, bombs rocked a federal tax building, Montreal’s Central Station and a CN Railway line over which the prime minister was about to travel. Veteran William Vincent O’Neill was killed by a bomb at an army recruiting centre.</p>

                  <p class="fadein"> More bomb experts were needed. Trained by the Royal Canadian Engineers, a new bomb squad, including Côté, started work on May 10.</p>
                      
                    <p class="fadein"> Fifteen bombs were planted in 11 mailboxes in Westmount, an affluent anglophone neighbourhood, on May 17. They were made of three sticks of dynamite, a small battery and a detonator wired to a Dax pocket watch, reports D’Arcy Jenish in <i>The Making of the October Crisis.</i> Five blew up in the middle of the night. </p>
                      
                    <p class="fadein"> Army engineers were called in to help. After defusing two, a third bomb blew up in Sergeant Walter Leja’s hands. He lost an arm, suffered brain damage and paralysis, lost hearing and speech and spent most of the rest of his life at the veterans’ hospital in Sainte-Anne-de-Bellevue, dying in 1992.</p>
                      
                    <p class="fadein">Côté often handled dynamite bombs bare-handed, snipping the wire connecting the timing device to the detonator. In his car trunk, he kept an awkward, hot, armoured suit weighing 38 kilograms, manufactured by Spooner Armor Protective Equipment in the United States. It was put to good use. </p>
                      
                    <p class="fadein">Côté defused his last bomb on July 12, 1970—45 kilograms of dynamite in a car left in the alley connecting the four buildings of the Bank of Montreal’s head office.</p>
                      
                    <p class="fadein">“Had I failed that day, they would have scraped our remains off the walls and sent them to three or four funeral homes,” said Côté in Jenish’s book. </p>

