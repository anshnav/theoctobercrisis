                      <p class="fadein"><span class="drop-cap">R</span><b>obert Côté</b> of Montreal owes his life to these $3.95 wire-cutting pliers he bought at a hardware store in 1963. He used them to defuse dozens of bombs during the seven-year terror campaign of the Front de libération du Québec.

                        <p class="fadein">Côté, retired head of Montreal’s police bomb squad, estimated that about a third of FLQ bombs were defused or detonated by experts (or were duds).</p>
                        
                          <p class="fadein">But “there were 200 bombs that did explode,” said Xavier Gélinas, curator of political history at the Canadian Museum of History. In Montreal in the 1960s, bombs “were always in our mind; always, constantly.” 

                          </p>
                        
                            <p class="fadein">The FLQ embraced violence to achieve political and social reform—armed robbery, bombings, hijackings, kidnappings and murder. Eight people were killed in the violence, dozens maimed and hundreds injured. 
                               </p>
                        
                           <p class="fadein">The FLQ targeted symbols of colonialism and English-speaking businesses and institutions, but its victims were often ordinary working people. The bomb was its signature weapon. It used Molotov cocktails in firebombings and pilfered dynamite from construction sites scattered across the city in the building boom preceding Expo 67.</p>
                        
                          <p class="fadein">Bombs were set at the homes of the mayor and the police chief, the city’s main communications tower, RCMP headquarters, military sites, city hall, a Royal Canadian Legion hall, the Montreal Stock Exchange, schools and McGill University. </p>
                  </p>
