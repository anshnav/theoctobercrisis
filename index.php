<div class="section-1 section-6">

    <div class="img-container fadein">
    
                <div class="blockquote">   
                    <p>“For us, it was hard to hear about the ‘occupation of Quebec.”</p>
                </div>
        </div>
       

    <div class="text-section">
                <p class="fadein">Most of those who served during the October Crisis were on duty seven days a week and put in a lot of overtime. But they made it home for Christmas. On Dec. 3, the kidnappers holding James Cross agreed to release the diplomat in exchange for a flight to Cuba and years of exile. By the end of 1970, all four Laporte kidnappers were behind bars. They eventually received prison sentences ranging from eight years to life.</p><br>

                <p class="fadein">That was the end of the FLQ, but not the story. At the time, the vast majority of Canadians, including Quebecers, supported the <i>War Measures Act</i>, the arbitrary arrest of nearly 500 people and the presence of soldiers on the streets. But, with the passage of time, sovereigntist politicians and sympathizers have cast these events in a darker light, asserting that the federal government used the October Crisis as a pretext for crushing the independence movement. They refer to the role of the military as “the occupation of Quebec,” which doesn’t sit well with the troops who served their country and their fellow citizens. </p><br>

                <p class="fadein">“For us, it was hard to hear about the ‘the occupation of Quebec,’” said Boucher. “After the crisis, people were really upset. When we were with our families, the discussion would start. Why would the federal government get involved in this? Why did you go? We were caught between the two. We served the federal government, but we were from Quebec. We had to defend our people.”</p> <br>

    </div>
        
  
