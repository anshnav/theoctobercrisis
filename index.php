        <div class="text-section">
                    <p class="fadein">Bourassa requested military assistance on the afternoon of Thursday, Oct. 15—some 14 hours before the proclamation of the <i>War Measures Act</i>—to bring a measure of control over a crisis that was rapidly escalating. </p><br>

                    <p class="fadein">Ten days had elapsed since the Cross kidnapping and five since Laporte was taken outside his home in the South Shore community of Saint-Lambert. The police had no idea where the two men were being held. Hundreds of officers frantically searched for them.</p><br>

                    <p class="fadein">Thousands of college and university students were preparing to leave their classrooms to demonstrate support for the kidnappers. Quebec authorities—having dealt with numerous violent demonstrations as well as several riots in the years leading up to October 1970—feared that demonstrations would lead to rioting, bloodshed and chaos in the streets of Montreal or elsewhere.</p> <br>

                    <p class="fadein">The federal government quickly agreed to Quebec’s request for military assistance and, within hours, soldiers were on the move from CFB Valcartier, CFB Edmonton and elsewhere. Some of those soldiers—young men at the time, now aging and long retired—still have vivid memories of those tense days.</p>
        </div>
            
      
  </div> 





<div class="section-1 section-3">

    <div class="img-container fadein">
    
                <div class="blockquote">   
                    <p>Within hours, soldiers were on the move from CFB Valcartier, CFB Edmonton and elsewhere. </p>
                </div>
        </div>
       

    <div class="text-section">
                <p class="fadein"><b>Jean-Guy Bernard</b> was a captain with the Royal 22nd Regiment—the Valcartier-based Van Doos. </p><br>

                <p class="fadein">“I had just graduated as a captain from the staff college in Kingston,” he said. “I was in Valcartier at the time the crisis began and I deployed the brigade.” Those Van Doos headed west on Highway 20, the Trans-Canada Highway, in a convoy of 200 trucks destined for CFB Montreal, a supply base commonly known as Longue-Pointe.</p><br>

                <p class="fadein">“I was at a function in the officers’ mess in Edmonton and received a message to go home and get [my] gear ready,” said Dave Krauter, who was 24 at the time and a lieutenant with the Canadian Airborne Regiment. “We flew out at midnight. There were C-130 Hercules aircraft all lined up with ramps down. We got off buses and straight onto the aircraft.”</p> <br>

                <p class="fadein">All told, some 1,100 soldiers boarded six aircraft destined for CFB St. Hubert, located in the South Shore suburb of the same name. They landed around 6 a.m. the following day and the normally quiet base was soon buzzing with activity. Little did anyone know that the FLQ’s four-member Chénier Cell was holding Laporte in a shabby bungalow at 5630 rue Armstrong, less than two kilometres from the base.</p><br>

                <p class="fadein">Laporte’s ordeal ended late in the afternoon of Saturday, Oct. 17. He made a frantic attempt to escape and, while trying to restrain him, two of the kidnappers strangled him. They dumped his body in the trunk of a beat-up blue Chevrolet and abandoned the vehicle in the parking lot of Won-Del Aviation, a small company within sight of CFB St. Hubert.</p>
    </div>
        
  
</div> 




<div class="section-1 section-4">

    <div class="img-container fadein">
    
                <img class="img-1" src="images/laporte-3.jpg" alt="">
            
                <div class="image-description">
                        
                        <p class="description-text">Quebec’s Minister of Labour and Manpower Pierre Laporte is surrounded by his family as he speaks to the media from his Montreal home.</p>
                        <p class="description-text" style="font-size: 0.6rem; color: gray; margin-top: 0.5rem;">The Canadian Press/Montreal Gazette/06075387</p>
                </div>
        </div>
       

    <div class="text-section">
                <p class="fadein">The murder shocked the nation, led to a collective outpouring of grief in Quebec, and raised tensions to new levels. Tensions subsided, however, as days turned to weeks without any resolution or escalation of the crisis. Soldiers spent days and nights on guard duty, sometimes shivering in the cold, sometimes shaking off tedium. “We worked in 24-hour shifts,” said Krauter. “For 24 hours, you weren’t able to take off your boots.” </p><br>

                <p class="fadein">“I was on duty for 24 hours, guarding the home of Bourassa’s sister-in-law,” said former Van Doo René Boucher. “We would have one guy inside their home and one guy outside 24 hours a day.”</p><br>

                <p class="fadein">Apart from guard duty, soldiers conducted joint operations—usually searches—with police officers. The provincial Sûreté du Québec (SQ) established a command centre at its headquarters on rue Parthenais in east-end Montreal and senior military officers worked with their SQ counterparts and the Montreal Police Service to plan operations.</p> <br>

                <p class="fadein">When conducting searches, soldiers formed a cordon around a building or area to be searched and prevented anyone from entering or leaving while police officers looked for FLQ suspects.</p><br>

                <p class="fadein">“One of the searches was in a wooded area on the island of Montreal,” recalls Bill Tremain, then 22 and a Canadian Airborne Regiment member. “It was early morning, around 4 a.m., and the sun was just starting to come up.</p><br>

                <p class="fadein">“We were all carrying loaded weapons and I’m standing on this pathway and out comes this Volkswagen Beetle with a guy and a girl in it. An officer ordered the vehicle to stop, but the guy’s yelling at me in French. We don’t speak French. They don’t speak English. The guy stops and rolls down his window and the officer puts a gun to his head and yells, ‘Get out now.’ He understood that. These two were out in the woods having some fun and were driving out in the morning.”</p>
    </div>
        
  
</div> 




<div class="section-1 section-5">

    <div class="img-container fadein">
    
                <img class="img-1" src="images/cross.jpg" alt="">
            
                <div class="image-description">
                        
                        <p class="description-text">British Trade Commissioner James Cross plays solitaire while he sits in captivity.</p>
                        <p class="description-text" style="font-size: 0.6rem; color: gray; margin-top: 0.5rem;">The Canadian Press/905916</p>
                </div>
        </div>
       

    <div class="text-section">
                <p class="fadein">Some 5,000 troops were deployed to protect Hydro-Québec property and armouries and other military installations, said Bernard. The FLQ may have posed little or no threat beyond the large cities,  but many residents of smaller centres were still on edge.  </p><br>

                <p class="fadein">“At the time, it was hunting season,” said Bernard. “We were receiving reports of machine-gun fire from residents in rural areas. We had to send reconnaissance patrols out to confirm the shots were fired by hunters.”</p><br>

                <p class="fadein">Troops deployed in Ottawa, meanwhile, protected some of the leading figures in Canadian politics. Jim Hemlin, a member of the Royal Canadian Horse Artillery stationed at CFB Petawawa, was on duty in the nation’s capital from mid-October until shortly before Christmas. At various times, he was assigned to John Diefenbaker and his wife Olive, John Turner and his family, Governor-General Roland Michener, and Eleanor (Nelly) Adams Martin, the wife of Paul Martin Sr.</p> <br>

                <p class="fadein">“We were sitting in Mrs. Martin’s family room one day watching TV and she asked when was the last time you talked to your wife,” said Hemlin. “I said I haven’t talked to her since I came down here. She said: ‘What’s your phone number. You’re going to talk to her right now.’ She dialed and handed me the phone.”</p><br>

    </div>
        
  
</div> 






