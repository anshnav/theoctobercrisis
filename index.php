<div class="section-1" id="section-1">


        <div class="text-section">
                <p class="fadein">On the morning of Monday, Oct. 5, 1970, four young men riding in a stolen taxi cab pulled up and stopped abruptly in front of 1297 Redpath Crescent—a stately, two-storey executive-style home that stood high on the slopes of Mount Royal, overlooking downtown Montreal, the Saint Lawrence River and the South Shore suburbs beyond. This was the home of British Trade Commissioner James Cross, his wife Barbara and their daughter Susan. </p> <br>
      
                <p class="fadein">Three men jumped out of the cab. One was dressed as a delivery man. He sprinted up a set of concrete steps to the front door carrying a long, slender, gift-wrapped package. He knocked and the Cross family’s Portuguese maid—a woman cradling a baby in one arm—answered.</p> <br>
                      
                <p class="fadein">“Birthday present for Mr. Cross,” the young man said. “You’ll have to sign for it.”
                      “I don’t have a pen,” the maid said.
                      “Here’s one,” he said as he pulled a revolver from under his coat. His two accomplices dashed up the steps. One acted as a lookout. The other tore the gift wrap from the package, revealing a submachine gun.</p><br>
                      
                <p class="fadein"> The gunmen hustled the terrified maid up to the master bedroom. James Cross was getting dressed and discussing the week ahead with his wife. They marched him out of the house at gunpoint, put him in the back of the cab, made him lie face down on the floor and sped away.</p>
        </div>



        <div class="img-container fadein">
        
                <img class="img-1" src="images/img-1.jpg" alt="">
    
                        <div class="image-description">
                                <p class="description-heading">Plotting the crime</p>
                                <p class="description-text">FLQ members (from left) Paul Rose, Jacques Rose, Francis Simard and Jacques Beaulne discuss their plans.</p>
                                <p class="description-text" style="font-size: 0.6rem; color: gray; margin-top: 0.5rem;">Keystone Pictures USA/ZUMAPRESS/AlamyQuebec's Minister of Labour and Manpower Pierre Laporte</p>
                        </div>
            </div>
           

        <div class="text-section">
                    <p class="fadein">So began the October Crisis of 1970. The kidnapping of James Cross—by militants of the Front de libération du Québec—unleashed a chain of events that shook and shocked our nation. </p><br>

                    <p class="fadein">These included the kidnapping five days later of Pierre Laporte, a senior cabinet minister in Premier Robert Bourassa’s government, his brutal murder at the hands of his abductors, and the proclamation of the <i>War Measures Act</i> by the federal government of Prime Minister Pierre Trudeau.</p><br>

                    <p class="fadein">That was followed by the arrest and detention of nearly 500 people, most of whom were never charged with criminal offences. Combat-ready soldiers began guarding politicians and other prominent public figures and government buildings and military installations in Ottawa, Montreal, Quebec City and several smaller Quebec centres.</p> <br>

                    <p class="fadein">Soldiers appeared first in Ottawa—the date was Oct. 13—and that led to perhaps the most famous confrontation in Canadian history between a journalist and a prime minister.</p>
        </div>
            
      
  </div> 





